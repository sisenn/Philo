#include "philo.h"

int	ft_atoi(char *s)
{
	int	i;
	int	j;
	int	temp;

	i = 0;
	j = 1;
	temp = 0;
	while (s[i] <= 32)
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			j *= -1;
		i++;
	}
	while (s[i] >= 32)
	{
		temp = (temp * 10) + (s[i] - 48);
		if (temp * j > 2147483647)
			return (0);
		if (temp * j < -2147483648)
			return (-1);
		i++;
	}
	return (temp * j);
}
